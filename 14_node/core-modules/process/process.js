// Returns the effective group identity of the Node.js process.

// process.getgid()
// Returns the group identity of the Node.js process.

// process.getgroups()
// Returns an array of the supplementary group IDs associated with the Node.js process.

// process.getuid()
// Returns the user identity of the Node.js process.

// process.hrtime.bigint()
// Returns the current high-resolution real time in nanoseconds.

// process.hrtime()
// Returns the current high-resolution real time in seconds and nanoseconds.

// process.kill(pid[, signal])
// Sends a signal to the process specified by pid.

// process.memoryUsage()
// Returns an object describing the memory usage of the Node.js process.

// process.nextTick(callback[, ...args])
// Queues the specified callback to be executed in the next iteration of the event loop.

// process.pid
// Returns the PID (process identifier) of the Node.js process.

// process.platform
// Returns a string identifying the operating system platform.

// process.ppid
// Returns the PID of the parent process.

// process.release
// An object containing metadata about the Node.js release.

// process.report
// An object containing methods for generating diagnostic reports.

