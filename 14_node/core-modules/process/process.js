// process.arch
// Returns a string identifying the CPU architecture.

// process.argv
// An array containing the command-line arguments passed when the Node.js process was launched.

// process.cwd()
// Returns the current working directory of the Node.js process.

// process.env
// An object containing the user environment variables.

// process.exit([code])
// Exits the current Node.js process with the specified exit code.

// process.exitCode
// A property to set or get the exit code of the process.

// process.emitWarning(warning[, options])
// Emits a warning with the provided warning message.

